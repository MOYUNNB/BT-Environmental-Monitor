/*
 * ============================================================
 *  学习笔记: ST7789V2 显示原理
 * ============================================================
 *
 * TFT-LCD 基础:
 *   ST7789V2 是一款 262K 色 (18-bit) 的单芯片 TFT-LCD 驱动器。
 *   分辨率为 240RGB x 320 像素。
 *
 *   "TFT" = Thin Film Transistor (薄膜晶体管)
 *   每个像素由 3 个子像素 (R/G/B) 组成, 每个子像素由一个
 *   TFT 控制。当 TFT 导通时, 对应的液晶分子偏转, 改变
 *   偏振光方向, 从而控制透光率。
 *
 *   "240RGB" 的含义:
 *   240 列 × 320 行 × 每像素 3 色 (R/G/B) = 230400 个子像素
 *   每个子像素有自己的 TFT 开关。
 *
 *   ST7789V2 内部有 GRAM (Graphics RAM):
 *   240 x 320 = 76800 个像素的存储空间。
 *   写入 GRAM 的数据会自动刷新到屏幕上。
 *   所以: 你只需要修改 GRAM 的内容, 屏幕会自动更新。
 *   不需要像老式 LCD 那样持续刷新。
 *
 * ============================================================
 *  学习笔记: SPI 驱动方式 — 命令 / 数据模式
 * ============================================================
 *
 * ST7789V2 通过 SPI 接口通信, 使用 DC (Data/Command) 引脚
 * 区分发送的是命令还是数据:
 *
 *   DC = 0: 发送的是命令 (Command)
 *     如: 0x01 (SWRESET), 0x11 (SLPOUT), 0x29 (DISPON)
 *     命令通常后跟 0~N 个字节的参数
 *
 *   DC = 1: 发送的是数据 (Data)
 *     如: 颜色值 (RGB565), 坐标参数, 命令参数
 *     数据可以在一个命令后连续发送
 *
 * SPI 传输时序:
 *   ┌─────────────────────────────────────────────────────┐
 *   │ 写命令:                                              │
 *   │   CS 拉低 → DC 拉低 → SPI 发 1 字节命令 → CS 拉高    │
 *   │                                                     │
 *   │ 写数据:                                              │
 *   │   CS 拉低 → DC 拉高 → SPI 发 1~N 字节数据 → CS 拉高   │
 *   │                                                     │
 *   │ 组合: 命令+参数                                      │
 *   │   CS 拉低 → DC 拉低 → SPI 发命令                     │
 *   │   → DC 拉高 → SPI 发参数 1 → ... → SPI 发参数 N      │
 *   │   → CS 拉高                                          │
 *   │                                                     │
 *   │ 注意: CS 在整个命令+参数传输过程中保持低电平!         │
 *   │ 上面的辅助函数为了简化, 每发一个字节拉一次 CS,         │
 *   │ 但实际高效的实现应该让 CS 保持低直到所有数据传输完成。  │
 *   └─────────────────────────────────────────────────────┘
 *
 * ============================================================
 *  学习笔记: RGB565 颜色编码
 * ============================================================
 *
 * ST7789V2 支持 12-bit (RGB444), 16-bit (RGB565), 18-bit (RGB666)
 * 三种像素格式。本驱动使用最常用的 RGB565 (16-bit)。
 *
 * RGB565 的 16 位分配:
 *   位 [15:11] (5 bit):  Red   — 0~31
 *   位 [10:5]  (6 bit):  Green — 0~63
 *   位 [4:0]   (5 bit):  Blue  — 0~31
 *
 *   16 进制表示: 0b RRRR RGGG GGGB BBBB
 *
 * 颜色值构造方法:
 *   #define RGB565(r, g, b)  (((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3))
 *   其中 r, g, b 是 8 位 (0~255) 的颜色分量。
 *   8 位红 → 5 位: 右移 3 位 (取高 5 位)
 *   8 位绿 → 6 位: 右移 2 位 (取高 6 位)
 *   8 位蓝 → 5 位: 右移 3 位 (取高 5 位)
 *
 * 预定义颜色:
 *   WHITE   = 0xFFFF = 11111 111111 11111 = 全亮白色
 *   BLACK   = 0x0000 = 00000 000000 00000 = 全黑
 *   RED     = 0xF800 = 11111 000000 00000
 *   GREEN   = 0x07E0 = 00000 111111 00000
 *   BLUE    = 0x001F = 00000 000000 11111
 *   YELLOW  = 0xFFE0 = 11111 111111 00000 (红+绿)
 *   CYAN    = 0x07FF = 00000 111111 11111 (绿+蓝)
 *   MAGENTA = 0xF81F = 11111 000000 11111 (红+蓝)
 *
 * 注意: Green 有 6 bit 精度, 因为人眼对绿色最敏感。
 *       RGB565 一共 65536 种颜色, 适合嵌入式应用。
 *
 * ============================================================
 *  学习笔记: 初始化指令序列 (每一步的作用)
 * ============================================================
 *
 * ST7789V2 上电后需要按照特定顺序发送初始化命令,
 * 顺序错了会导致显示异常 (黑屏、花屏、方向错误等).
 *
 * 标准初始化序列:
 *
 * ┌─────────────┬─────────┬────────────────────────────────┐
 * │ 命令 (HEX)  │ 参数    │ 作用                            │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x01        │ 无      │ SWRESET — 软件复位              │
 * │             │         │ 复位内部状态机, 恢复默认值。      │
 * │             │         │ 注意: 硬件复位 RST 引脚已经       │
 * │             │         │ 做过复位, 这里再发一次软件复位     │
 * │             │         │ 确保状态干净。                    │
 * │             │         │ 后需延时 150ms 等待复位完成。      │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x11        │ 无      │ SLPOUT — 退出睡眠模式            │
 * │             │         │ 上电后 ST7789V2 处于睡眠模式,     │
 * │             │         │ 只有发送此命令后才会开始工作。     │
 * │             │         │ 注意: 不是立即生效, 需要延时       │
 * │             │         │ 至少 120ms (典型 200ms) 等待       │
 * │             │         │ 内部振荡器稳定。                  │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x36        │ 1字节   │ MADCTL — 内存访问控制            │
 * │             │         │ 设置显示方向、RGB/BGR 顺序。       │
 * │             │         │ 参数 bit:                         │
 * │             │         │   bit7  = MY: 行地址反转          │
 * │             │         │   bit6  = MX: 列地址反转          │
 * │             │         │   bit5  = MV: 行列交换            │
 * │             │         │   bit4  = ML: 行输出模式 (上/下)   │
 * │             │         │   bit3  = RGB/BGR 选择 (0=RGB)    │
 * │             │         │ 竖屏: 0x00, 横屏: 0x60 (MX|MV)   │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x3A        │ 1字节   │ COLMOD — 像素格式设置             │
 * │             │         │ 0x55 = 16-bit (RGB565)           │
 * │             │         │ 0x66 = 18-bit (RGB666)           │
 * │             │         │ 0x53 = 12-bit (RGB444)           │
 * │             │         │ 0x55 是最常用的, 16 位/像素。     │
 * │             │         │ 注意: 必须和发送数据时的格式      │
 * │             │         │ 一致, 否则颜色会错乱。             │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x2A        │ 4字节   │ CASET — 列地址设置               │
 * │             │         │ 设置 GRAM 写入的列范围。          │
 * │             │         │ 参数: XS, XE (各 2 字节, Big Endian)│
 * │             │         │ 全屏: 0x00,0x00,0x00,0xEF        │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x2B        │ 4字节   │ RASET — 行地址设置               │
 * │             │         │ 类似 CASET, 设置行范围。          │
 * │             │         │ 全屏: 0x00,0x00,0x01,0x3F        │
 * ├─────────────┼─────────┼────────────────────────────────┤
 * │ 0x29        │ 无      │ DISPON — 开显示                  │
 * │             │         │ 打开显示, 屏幕亮起。              │
 * │             │         │ 在这之前屏幕是黑的。              │
 * │             │         │ 后需延时 50ms。                   │
 * └─────────────┴─────────┴────────────────────────────────┘
 *
 * 可选命令 (根据屏幕型号可能需要):
 *   0x21: INVON — 颜色反相 (部分屏幕需要)
 *   0x13: NORON — 正常显示模式 (部分屏幕需要退出部分模式)
 *   0xB0: RAMCTRL — RAM 控制 (调整像素时钟)
 *   0xC0~0xC5: 电源控制系列 (伽马、VCOM 等)
 *
 * ============================================================
 *  学习笔记: 画窗模式 (CASET + RASET + RAMWR)
 * ============================================================
 *
 * ST7789V2 使用"画窗"机制, 这是最核心的显示概念:
 *
 *   步骤 1: CASET (0x2A) — 设置列 (X) 范围
 *   步骤 2: RASET (0x2B) — 设置行 (Y) 范围
 *   步骤 3: RAMWR (0x2C) — 开始写入像素数据
 *
 * 然后发送的每个像素数据会自动填入窗口内,
 * 从左到右, 从上到下, 填满窗口后自动停止。
 *
 * 示例: 在 (10,20) 处画一个红点:
 *   CASET(10, 10)  // 列范围只包含第 10 列
 *   RASET(20, 20)  // 行范围只包含第 20 行
 *   RAMWR → 发送 0xF800 (RED)  // 写入该位置
 *
 * 示例: 填充整个屏幕为蓝色:
 *   CASET(0, 239)  // 所有列
 *   RASET(0, 319)  // 所有行
 *   RAMWR → 发送 240×320 = 76800 次 0x001F (BLUE)
 *
 * 为什么叫"画窗"?
 *   想象你有一个喷漆枪 (RAMWR) 和一张遮罩 (CASET+RASET)。
 *   遮罩开一个矩形窗口, 喷漆枪只能在窗口内喷。
 *   遮罩设置一次, 喷漆可以连续进行。
 *
 * 这种模式的巨大优势:
 *   不需要每次都指定 X/Y 坐标, 通过 SPI 连续发送像素即可。
 *   对于刷屏操作, 一次 CASET+RASET 设置 → 然后连续 DMA 发送 76800 像素。
 *   CPU 零占用。
 *
 * ============================================================
 *  学习笔记: 5x7 字库实现思路
 * ============================================================
 *
 * ASCII 字符在 LCD 上显示, 需要将字符转换为像素点阵。
 * 最常用的是 5x7 字体 (每个字符占 5 列 × 7 行像素)。
 *
 * 数据存储: 每个字符用 5 个字节表示, 每字节的 8 bit 中
 * 用低 7 bit 表示 7 行像素 (bit0=最上行)。
 *
 * 示例: 字符 'A' (0x41) 的点阵:
 *
 *   二进制      十六进制    像素图 (1=亮, 0=暗)
 *   00100       0x20        ..@..
 *   01010       0x50        .@.@.
 *   10001       0x88        @...@
 *   11111       0xF8        @@@@@
 *   10001       0x88        @...@
 *   10001       0x88        @...@
 *   00000       0x00        .....
 *
 * 所以 'A' 的 5 字节字模数据: {0x20, 0x50, 0x88, 0xF8, 0x88}
 *
 * 在 LCD 上显示字符 'A':
 *   1. 遍历 5 个字节 (每列一个)
 *   2. 对每个字节, 遍历低 7 bit
 *   3. 如果 bit=1 → 画前景色, bit=0 → 画背景色
 *   4. 放大: scale=2 时每个像素变成 2x2 的方块
 *
 * 字库表: 通常包含 ASCII 码 0x20~0x7E (空格到 ~)
 *   共 95 个字符, 每个 5 字节, 约 475 字节。
 *   只需要存储一个 const uint8_t 数组即可。
 *
 * 常见字库来源:
 *   1. 自行制作 (用软件取模)
 *   2. 开源项目 (u8g2, Adafruit_GFX)
 *   3. 在线生成 (https://oleddisplay.squix.ch/)
 *
 * ============================================================
 *  学习笔记: 为什么 LCD 不需要 SPI 锁?
 * ============================================================
 *
 * 在这个项目中:
 *   SPI1 → 仅用于 LCD (独占)
 *   SPI2 → IMU + Flash (共享, 需要互斥锁)
 *
 * 因为 SPI1 只被 LCD 使用, 没有其他设备共享总线,
 * 所以 LCD 驱动中没有互斥锁。
 *
 * 如果未来 SPI1 接了多个设备 (如 LCD + SD 卡),
 * 就需要添加互斥锁保护。
 *
 * 但在任务层面, LCD 更新任务 (StartLCDUpdate) 是 200ms 周期,
 * 而 SPI1 的传输速度是 21MHz:
 *   传输 240×320 = 76800 像素 × 2 字节 = 153600 字节
 *   理论时间: 153600 × 8 / 21000000 ≈ 58.5ms
 *   实际加上命令开销: 约 70~80ms
 *   这比 200ms 周期短, 所以 LCD 更新不会影响其他任务。
 *
 * 注意: 如果 LCD 刷屏期间其他任务也需要快速响应,
 * 可以考虑将 LCD 刷屏使用 DMA, 并降低优先级。
 */

/* 屏幕尺寸 */
#define LCD_WIDTH               240U
#define LCD_HEIGHT              320U

/* RGB565 颜色定义 */
#define LCD_COLOR_WHITE         0xFFFFU
#define LCD_COLOR_BLACK         0x0000U
#define LCD_COLOR_RED           0xF800U
#define LCD_COLOR_GREEN         0x07E0U
#define LCD_COLOR_BLUE          0x001FU
#define LCD_COLOR_CYAN          0x07FFU
#define LCD_COLOR_MAGENTA       0xF81FU
#define LCD_COLOR_YELLOW        0xFFE0U
#define LCD_COLOR_GRAY          0x8430U
#define LCD_COLOR_ORANGE        0xFD20U

/* 显示方向 */
typedef enum {
    LCD_DIR_PORTRAIT = 0,       /* 竖屏 */
    LCD_DIR_LANDSCAPE = 1,      /* 横屏 (90°) */
    LCD_DIR_PORTRAIT_180 = 2,   /* 竖屏倒置 */
    LCD_DIR_LANDSCAPE_270 = 3,  /* 横屏倒置 (270°) */
} LCD_Direction_t;

/**
 * @brief  初始化 LCD
 * @note   TODO: 实现硬件复位 → 初始化指令序列 → 开背光
 *
 * 实现步骤:
 * 1. 硬件复位: RST 拉低 10ms → 拉高 120ms
 * 2. 发初始化指令 (参考上方注释中的命令序列)
 * 3. 设置显示方向 (默认竖屏)
 * 4. 开背光 (通过硬件 PWM 或 GPIO)
 * 5. 清屏为黑色
 *
 * 提示: 如果屏幕不亮, 先检查:
 *   - 背光使能引脚是否配置正确
 *   - 初始化序列是否完整
 *   - MADCTL 参数是否匹配屏幕安装方向
 */
void LCD_Init(void);

/**
 * @brief  设置显示方向
 * @param  dir: 方向枚举
 *
 * 实现: 修改 MADCTL(0x36) 的参数
 *   MV 位 = 行列交换 (竖屏→横屏)
 *   MX 位 = 列反向
 *   MY 位 = 行反向
 *
 * 不同方向的 MADCTL 值:
 *   竖屏 (默认):       0x00
 *   横屏 (旋转 90°):   0x60 (MV|MX)
 *   竖屏翻转 (180°):   0xC0 (MY|MX)
 *   横屏翻转 (270°):   0xA0 (MV|MY)
 *
 * 注意: 改变方向后, 屏幕的宽/高也会交换。
 *       画窗时需要注意 LCD_WIDTH/LCD_HEIGHT 的变化。
 */
void LCD_SetDirection(LCD_Direction_t dir);

/**
 * @brief  清屏
 * @param  color: RGB565 颜色值
 * @note   TODO: 实现全屏填充
 *
 * 实现思路:
 * 1. CASET(0, 239) + RASET(0, 319) — 设置全屏窗口
 * 2. RAMWR — 连续发送 240×320 = 76800 次 color
 *
 * 效率优化:
 *   用 HAL_SPI_Transmit 一次发送大缓冲区, 而不是每次发 2 字节。
 *   因为 SPI 每次传输都有固定开销 (CS 拉高拉低, 中断处理等)。
 *   最好的做法: 创建一个 76800×2 = 153600 字节的缓冲区,
 *   全部填充 color, 然后一次 SPI 发送。
 *   但 153600 字节 ≈ 150KB 可能超出 RAM 限制。
 *
 * 折中方案:
 *   使用较小的缓冲区 (如 1024 字节), 分多次 SPI 发送。
 *   或者使用 SPI DMA + 双缓冲。
 *   最简方案 (初学者): for 循环每次发 2 字节 (效率低但简单)。
 */
void LCD_Clear(uint16_t color);

/**
 * @brief  画一个像素点
 * @param  x: X 坐标 (0~239)
 * @param  y: Y 坐标 (0~319)
 * @param  color: RGB565 颜色值
 * @note   TODO: 实现设置窗口 → 写像素
 *
 * 实现:
 *   1. CASET(x, x) — 列窗口只有一列
 *   2. RASET(y, y) — 行窗口只有一行
 *   3. RAMWR → 发送 color (2 字节)
 *
 * 这是所有图形函数的基础:
 *   LCD_DrawString  → 调用 LCD_DrawPixel 多次
 *   LCD_DrawLine    → 调用 LCD_DrawPixel 多次 (Bresenham 算法)
 *   LCD_FillRect    → CASET+RASET 设置矩形, 然后批量写 color
 *
 * 注意: 频繁调用 DrawPixel 效率很低 (3 次 SPI 事务/像素)。
 *       对于批量操作 (如 FillRect), 直接用画窗模式。
 */
void LCD_DrawPixel(uint16_t x, uint16_t y, uint16_t color);

/**
 * @brief  填充矩形区域
 * @param  x0,y0: 左上角
 * @param  x1,y1: 右下角
 * @param  color: RGB565 颜色值
 *
 * 实现:
 *   1. CASET(x0, x1) + RASET(y0, y1) — 设置矩形窗口
 *   2. RAMWR → 连续发送 color (count × 2 字节)
 *      count = (x1-x0+1) × (y1-y0+1)
 *
 * 可以用来画:
 *   进度条、按钮、背景、边框等
 *   LCD_DrawProgressBar 就是基于此函数实现
 */
void LCD_FillRect(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1, uint16_t color);

/**
 * @brief  显示字符串 (ASCII)
 * @param  x,y: 起始坐标
 * @param  str: 字符串
 * @param  color: 字体颜色
 * @param  bg: 背景颜色
 * @param  scale: 字体放大倍数 (1~4)
 * @note   TODO: 实现至少 5x7 ASCII 字库 + 放大
 *
 * 实现步骤:
 * 1. 在代码中定义一个 5x7 字库数组:
 *    static const uint8_t s_font5x7[][5] = { ... };
 *    包含 ASCII 0x20~0x7E 的字模。
 *
 * 2. 遍历字符串:
 *    while (*str) {
 *        char c = *str++;
 *        if (c < 0x20 || c > 0x7E) c = ' ';  // 不可见字符替换为空格
 *        const uint8_t *ch = s_font5x7[c - 0x20];
 *
 *        // 遍历 5 列 × 7 行
 *        for (uint8_t col = 0; col < 5; col++) {
 *            for (uint8_t row = 0; row < 7; row++) {
 *                if (ch[col] & (1 << row)) {
 *                    // 画前景色 (放大的 scale × scale 方块)
 *                } else {
 *                    // 画背景色
 *                }
 *            }
 *        }
 *        x += 5 * scale + 1;  // 字符间距
 *    }
 *
 * 3. 放大实现:
 *    每个像素点变成 scale × scale 的方块。
 *    在 scale=1 时每个字符占 5×7 像素。
 *    在 scale=2 时每个字符占 10×14 像素。
 *
 * 优化建议:
 *   如果 LCD 长时间显示静态文字, 可以维护一个显存缓冲区
 *   (240×320×2 = 153,600 字节, 需要外部 SRAM),
 *   直接在缓冲区操作, 然后一次 DMA 刷新。
 */
void LCD_DrawString(uint16_t x, uint16_t y, const char *str,
                    uint16_t color, uint16_t bg, uint8_t scale);

/**
 * @brief  显示整数
 *
 * 实现: 用 sprintf 格式化为字符串, 然后调用 DrawString。
 */
void LCD_DrawInt(uint16_t x, uint16_t y, int32_t value,
                 uint16_t color, uint16_t bg, uint8_t scale);

/**
 * @brief  显示浮点数 (保留 decimals 位小数)
 *
 * 实现: 用 sprintf 格式化为字符串, 然后调用 DrawString。
 * 注意: float 的 sprintf 会占用较多代码空间 (约 10KB)。
 */
void LCD_DrawFloat(uint16_t x, uint16_t y, float value, uint8_t decimals,
                   uint16_t color, uint16_t bg, uint8_t scale);

/**
 * @brief  画进度条
 * @param  x,y: 左上角
 * @param  w,h: 宽高
 * @param  percent: 百分比 (0~100)
 *
 * 实现: 基于 LCD_FillRect
 *   1. 画背景: FillRect(x, y, x+w-1, y+h-1, color_bg)
 *   2. 计算填充宽度: fill_w = w * percent / 100
 *   3. 画前景: FillRect(x, y, x+fill_w-1, y+h-1, color_fg)
 */
void LCD_DrawProgressBar(uint16_t x, uint16_t y, uint16_t w, uint16_t h,
                         uint8_t percent, uint16_t color_fg, uint16_t color_bg);

