/*
 * ============================================================
 *  学习笔记: 按键消抖原理 (Debouncing)
 * ============================================================
 *
 * 机械按键的本质问题 — 抖动 (Bouncing):
 *
 *   机械按键内部是两个金属弹片, 按下时弹片接触产生电信号。
 *   由于弹片是金属的, 接触瞬间会发生多次弹跳 (bounce),
 *   导致电信号在高低电平之间快速振荡。
 *
 *   抖动持续时间: 通常 5~20ms (取决于按键质量)
 *   抖动频率: 几 kHz 到几十 kHz
 *
 *   波形示意:
 *     按下瞬间          释放瞬间
 *       ↓               ↓
 *    ┌──┐ ┌┐┌┐ ┌──┐ ┌──────┐
 *    │  │ ││││ │  │ │      │
 *    │  │ ││││ │  │ │      │
 *    ──┘  └┘└┘ └──┘ └──────┘────────
 *      抖动区           抖动区
 *
 *   如果没有消抖, 一次按键可能被误认为多次按下。
 *
 * 软件消抖方法对比:
 *
 * 方法 1: 延时采样法 (最简单, 但不推荐)
 *   if (GPIO_ReadPin() == 0) {       // 检测到按下
 *       HAL_Delay(20);                // 延时等抖动过去
 *       if (GPIO_ReadPin() == 0) {   // 再次确认
 *           return KEY_PRESSED;
 *       }
 *   }
 *   缺点: HAL_Delay 是阻塞的, 浪费 CPU 时间。
 *         在 FreeRTOS 中, 高优先级任务会延迟执行。
 *         如果按键扫描周期是 10ms, 延时 20ms 会错过一个周期。
 *
 * 方法 2: 定时采样法 (常用, 但不够灵活)
 *   每 10ms 采样一次, 连续 3 次相同才确认。
 *   这种方法需要定时器驱动, 适合 RTOS 任务周期调用。
 *   我们用的是这个方法, 但加入了状态机改进。
 *
 * 方法 3: 状态机消抖 (本驱动采用, 推荐)
 *   用有限状态机, 每次采样都更新状态:
 *     状态 0 (等待按下): 检测到低电平 → count++, count≥3 → 确认按下
 *     状态 1 (等待释放): 检测到高电平 → count++, count≥3 → 确认释放
 *   优点: 非阻塞, 实时性好, 不需要额外的定时器
 *
 * ============================================================
 *  学习笔记: 状态机消抖算法详解
 * ============================================================
 *
 * 本驱动的消抖使用状态机 + 计数:
 *
 *   每次 KEY_Scan() 调用时:
 *     1. 读取按键引脚的原始电平
 *     2. 比较原始电平和当前"稳定"电平
 *     3. 如果相同 → 计数器 +1
 *     4. 如果不同 → 计数器清零, 更新原始值
 *     5. 如果计数器 ≥ 3 (连续 3 次一致):
 *        - 更新"稳定"电平为当前值
 *        - 如果稳定电平 = 0 (按下), 记录按键
 *        - 计数器清零 (或保持在最大值)
 *
 * 状态迁移:
 *
 *   释放稳定状态             按下稳定状态
 *   ┌────────┐ ────(稳定值=0)───→ ┌────────┐
 *   │ 高电平 │                    │ 低电平 │
 *   │ count=3│ ←──(稳定值=1)──── │ count=3│
 *   └────────┘                    └────────┘
 *      ↑  ↓                         ↑  ↓
 *   (采样=1) (采样=0)           (采样=1) (采样=0)
 *      ↑  ↓                         ↑  ↓
 *   count++ count=0              count=0 count++
 *
 * 为什么需要"连续 N 次一致"?
 *   抖动是随机的, 一次采样可能是噪声。
 *   连续 N 次 (N=3) 采样一致, 可以过滤掉大部分抖动和噪声。
 *   因为 KEY_Scan 的调用周期是 10ms, 3 次 = 30ms,
 *   这已经大于典型的按键抖动时间 (5~20ms)。
 *
 * 为什么 KEY_GetPressed 需要"读后自动清除"?
 *   如果不清除, 同一个按键按下事件会被多次读取。
 *   例如: LCD 更新任务每 200ms 读一次, 按键按了 1 秒,
 *   会被读取约 5 次, 导致页面切换 5 次。
 *   "读后自动清除"确保一个按键动作只被处理一次。
 *
 * ============================================================
 *  学习笔记: EC11 旋转编码器原理
 * ============================================================
 *
 * EC11 是一种增量式旋转编码器 (Incremental Rotary Encoder)。
 * 它输出两路相位相差 90° 的脉冲信号 (A 相和 B 相)。
 *
 * 内部结构:
 *   编码器内部有一个带触点的旋转盘 (或光栅盘)。
 *   旋转时, 触点依次与两个金属刷接触。
 *   触点间距使得 A、B 输出信号相差 90° 电气角度。
 *
 * 正转 (顺时针, CW) 波形:
 *           ┌──┐  ┌──┐  ┌──┐  ┌──┐
 *   A ──────┘  └──┘  └──┘  └──┘  └────
 *              ┌──┐  ┌──┐  ┌──┐
 *   B ────────┘  └──┘  └──┘  └──┘ ────
 *           ↑  A 上升沿时 B 为低电平
 *
 * 反转 (逆时针, CCW) 波形:
 *           ┌──┐  ┌──┐  ┌──┐  ┌──┐
 *   A ──────┘  └──┘  └──┘  └──┘  └────
 *        ┌──┐  ┌──┐  ┌──┐  ┌──┐
 *   B ──┘  └──┘  └──┘  └──┘  └──┘ ────
 *        ↑  A 上升沿时 B 为高电平
 *
 * 通过比较 A 和 B 的相位关系来判断旋转方向:
 *   正转: A 领先 B 90° (A 上升沿时 B=0)
 *   反转: B 领先 A 90° (A 上升沿时 B=1)
 *
 * ============================================================
 *  学习笔记: 查表法解码 (4-bit 索引)
 * ============================================================
 *
 * EC11 的解码有几种方法:
 *
 * 方法 1: 中断法 (在 A/B 的上升/下降沿触发中断)
 *   在 A 的中断服务函数中读 B 的电平 → 判断方向
 *   优点: 响应快, 不占用任务时间
 *   缺点: EC11 的抖动也会触发中断, 需要额外消抖
 *
 * 方法 2: 定时采样 + 查表法 (本驱动采用)
 *   每 10ms 采样一次 A 和 B 的电平,
 *   将 (上次的 AB 值 << 2) | (当前的 AB 值) 作为索引查表。
 *
 * 为什么是 16 项表?
 *   两相 (A/B) 各一个 bit, 组成 2-bit 状态。
 *   上次 2-bit + 当前 2-bit = 4-bit 索引 → 16 种组合。
 *   每种组合对应: 无变化、正转一步、反转一步、或非法状态。
 *
 * 方向表 (ec11_table[16]):
 *
 *   值: 0 = 无变化, 1 = CW (正转), -1 = CCW (反转)
 *
 *   索引       二进制(上一次_AB | 当前_AB)    方向
 *   ────────────────────────────────────────
 *   0          00_00                       0 (没转)
 *   1          00_01                       +1 (正转)
 *   2          00_10                       -1 (反转)
 *   3          00_11                       0 (非法, 跳太多)
 *   4          01_00                       -1 (反转)
 *   5          01_01                       0 (没转)
 *   6          01_10                       0 (非法, 跳太多)
 *   7          01_11                       +1 (正转)
 *   8          10_00                       +1 (正转)
 *   9          10_01                       0 (非法, 跳太多)
 *   10         10_10                       0 (没转)
 *   11         10_11                       -1 (反转)
 *   12         11_00                       0 (非法, 跳太多)
 *   13         11_01                       -1 (反转)
 *   14         11_10                       +1 (正转)
 *   15         11_11                       0 (没转)
 *
 * 为什么有非法状态?
 *   EC11 每次只变化一个 bit (A 或 B),
 *   两个 bit 同时变化意味着采样间隔太长 (丢失了中间状态)。
 *   对于非法状态, 查表得到 0, 忽略本次变化。
 *
 * ============================================================
 *  学习笔记: 为什么按键用 GPIO 中断 + 状态机比纯轮询省电?
 * ============================================================
 *
 * 轮询 (Polling):
 *   - 任务每 10ms 唤醒, 读 GPIO, 然后继续睡眠
 *   - 即使没有任何按键, CPU 也每 10ms 被唤醒一次
 *   - 但这是本项目的设计 (FreeRTOS 任务调度)
 *
 * 中断 (External Interrupt, EXTI):
 *   - 按键按下时触发中断, CPU 从低功耗模式唤醒
 *   - 没有按键时 CPU 可以进入 STOP 模式 (μA 级功耗)
 *   - 只有在按键按下时才处理
 *
 * 在本项目中为什么选轮询?
 *   1. 本项目的按键扫描任务已经是 FreeRTOS 任务,
 *      任务调度器本身会周期性唤醒, 顺带扫描按键。
 *   2. 中断方式需要配置 EXTI 和中断优先级, 稍复杂。
 *   3. 但中断方式可以用于"从 STOP 模式唤醒 MCU"。
 *
 * 在实际的低功耗产品中:
 *   1. MCU 大部分时间处于 STOP 模式
 *   2. 按键通过 EXTI 产生中断唤醒
 *   3. 唤醒后扫描确认 (消抖)
 *   4. 处理完继续进入 STOP
 *
 * 本项目 (桌面供电) 不需要低功耗, 轮询即可。
 *
 * ============================================================
 *  学习笔记: KEY_GetPressed 原子读清 (防止重复触发)
 * ============================================================
 *
 * KEY_GetPressed() 和 EC11_GetDelta() 都使用"读后自动清除"模式:
 *
 *   KeyID_t KEY_GetPressed(void) {
 *       KeyID_t k = s_pressed;    // 读取当前值
 *       s_pressed = KEY_NONE;     // 立即清除
 *       return k;                 // 返回读取的值
 *   }
 *
 * 为什么需要原子读清?
 *   看一个典型的使用场景:
 *
 *     // LCD 任务 (200ms 周期)
 *     void StartLCDUpdate(void *arg) {
 *         while(1) {
 *             KeyID_t key = KEY_GetPressed();
 *             if (key == KEY_1) {
 *                 page_switch();   // 切换到下一页
 *             }
 *             osDelay(200);
 *         }
 *     }
 *
 *   如果不读后清除, 当按键按下时:
 *     第 1 次: KEY_GetPressed() 返回 KEY_1 → 切页
 *     第 2 次: 还未释放, 又返回 KEY_1 → 又切页!
 *     第 3 次: 同上
 *   连续切页 5 次, 用户只按了一次按键!
 *
 *   读后清模式确保: 一次按键只触发一次动作。
 *   按键释放后, 需要再次按下才会产生新的 KEY_1。
 *
 * 这个模式在按键驱动中非常常见。
 * 它本质上是一个"一次性事件队列" (single-event queue):
 *   - 生产者 (KEY_Scan) 写入事件
 *   - 消费者 (其他任务) 读取并消费事件
 *   - 每个事件只能被消费一次
 *
 * 对于更复杂的情况 (需要同时处理多个按键),
 * 可以使用环形缓冲区 (Ring Buffer) 来代替单变量。
 */

/* 按键 ID (与硬件引脚对应) */
typedef enum {
    KEY_NONE = 0,
    KEY_1,          /* PA0  — 用户按键 1 */
    KEY_2,          /* PE8  — 用户按键 2 */
    KEY_3,          /* PC13 — 用户按键 3 (板载) */
} KeyID_t;

/* EC11 编码器方向 */
typedef enum {
    EC11_NONE = 0,
    EC11_CW,        /* 顺时针 (正转) */
    EC11_CCW,       /* 逆时针 (反转) */
} EC11_Dir_t;

/**
 * @brief  初始化按键和 EC11
 * @note   TODO: 清空状态变量
 *
 * 实现: 将所有状态变量初始化为"释放"状态。
 * 包括 s_keys[3] 和 s_ec11_last_ab。
 * 参考 key.c 中已实现的框架。
 */
void KEY_Init(void);

/**
 * @brief  按键 + EC11 扫描 (10ms 周期调用)
 * @note   TODO: 实现消抖状态机 + EC11 查表
 *
 * 此函数由 StartKeyScan 任务每 10ms 调用一次。
 *
 * 实现步骤:
 *   1. 读 3 个按键的原始电平 (key_read_raw)
 *   2. 每个按键运行消抖状态机:
 *      - 当前采样 == 稳定状态 → count++
 *      - 当前采样 != 稳定状态 → count=0, 记录新采样
 *      - count >= KEY_SAMPLE_CNT → 更新稳定状态
 *      - 如果稳定状态变为 0 (按下) → 记录到 s_pressed
 *   3. EC11 查表解码:
 *      - 读当前 AB
 *      - 查表 ec11_table[(s_ec11_last_ab << 2) | current_ab]
 *      - 将结果累加到 s_ec11_delta
 *      - 更新 s_ec11_last_ab
 */
void KEY_Scan(void);

/**
 * @brief  获取按下的按键 (一次性读取, 读后自动清除)
 * @return KeyID_t 本次扫描到的按键, KEY_NONE = 无按键
 *
 * 原子读清模式:
 *   调用一次返回一个按键事件。
 *   同一个按键不会重复触发。
 *
 * 在其他任务中:
 *   KeyID_t key = KEY_GetPressed();
 *   if (key == KEY_1) { ... }
 */
KeyID_t KEY_GetPressed(void);

/**
 * @brief  获取 EC11 累计步数 (读后重置为 0)
 * @return 正数 = 顺时针步数, 负数 = 逆时针步数
 *
 * EC11 是有触感定位的编码器:
 *   - 每转一格 (detent) 产生一个完整脉冲
 *   - 返回值为从上一次查询到现在的累计步数
 *   - 正数: 顺时针旋转了多少格
 *   - 负数: 逆时针旋转了多少格
 *
 * 应用场景:
 *   页面切换: delta > 0 → 下一页, delta < 0 → 上一页
 *   参数调整: 音量、亮度、阈值等值的增减
 */
int8_t EC11_GetDelta(void);

